    public VoucherJFrame() 
    {
        initComponents();
        
        model = (DefaultTableModel) jTable1.getModel();
        
        String [][] voucher = v.getFullVoucher();
        
        for(int i=0 ; i<voucher.length; i++)
        {
            if(voucher[i][0] != "-1")
            {
                String VoucherType = null;//儲存優惠型態的
                String VoucherPrice = null;
                
                if(voucher[i][1] != "-1")
                {
                    VoucherType = "折扣";
                    VoucherPrice = voucher[i][1]+"折";
                }
                else if(voucher[i][2] != "-1")
                {
                    VoucherType = "均一價";
                    VoucherPrice = "$"+voucher[i][2]+"元";
                }
                else if(voucher[i][3] != "-1")
                {
                    VoucherType = "減價";
                    VoucherPrice = "減"+voucher[i][3]+"元";
                }
                
                
                model.insertRow(model.getRowCount(),new Object [] {voucher[i][0], VoucherType, VoucherPrice});
                jTable1.setRowHeight(40);//設定行高
            }
        }
        
        
        //#####################align center################################################
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        //jTable1.getColumnModel().getColumn(0).setCellRenderer( centerRenderer );
        jTable1.getColumnModel().getColumn(1).setCellRenderer( centerRenderer );
        jTable1.getColumnModel().getColumn(2).setCellRenderer( centerRenderer );
        //##################################################################################
    }
    

