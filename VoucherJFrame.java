    private void jButtonDelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonDelMouseClicked
        int column = jTable1.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/jTable1.getRowHeight();
        
        if(row < jTable1.getRowCount() && row >= 0 && column < jTable1.getColumnCount() && column >= 0 && jTable1.getSelectedRow() != -1)
        {
            Object rowValue = jTable1.getValueAt(row, column);
            DefaultTableModel tableModel = (DefaultTableModel) this.jTable1.getModel();
            
            int [] selectedRows = jTable1.getSelectedRows();
            
            for(int i=0;i<selectedRows.length;i++)
            {
                tableModel.removeRow(selectedRows[i]);
                
                v.deleteVoucher(selectedRows[i]);
                //System.out.println(selectedRows[i]);
            }
        }
    }//GEN-LAST:event_jButtonDelMouseClicked

