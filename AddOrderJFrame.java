    private void jButtonSetAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSetAddMouseClicked
        model = (DefaultTableModel) jTable1.getModel();
        
        String id = jComboBoxSetID.getSelectedItem().toString();
        String item = jComboBoxSetMain.getSelectedItem().toString();
        
        if(!item.equals("-"))
        {
            o1.addOrder(id, "套餐");
            o1.addOrder(item, "套主");

            String [] setmeal = o1.getMeal(o1.getItemCode(id));
            
            if(v.getVoucherId(o1.getItemCode(id)) != -1)
            {
                //有優惠的時候
                model.insertRow(model.getRowCount(),new Object [] {"(套) "+id,item,String.valueOf(v.Calc(v.getVoucherId(o1.getItemCode(id)),o1.getPrice(o1.getItemCode(id)))),""});
            }
            else
            {
                model.insertRow(model.getRowCount(),new Object [] {"(套) "+id,item,setmeal[5],""});
            }
            
            jTable1.setRowHeight(40);//設定行高

            if(!setmeal[2].equals(""))
            {
                model.insertRow(model.getRowCount(),new Object [] {"",o2.getItemName(setmeal[2]),"0",""});
            }
            if(!setmeal[3].equals(""))
            {
                model.insertRow(model.getRowCount(),new Object [] {"",o2.getItemName(setmeal[3]),"0",""});
            }
            if(!setmeal[4].equals(""))
            {
                model.insertRow(model.getRowCount(),new Object [] {"",o2.getItemName(setmeal[4]),"0",""});
            }
        }
    
    }//GEN-LAST:event_jButtonSetAddMouseClicked

