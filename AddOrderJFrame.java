    private void jButtonSingleAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSingleAddMouseClicked
        model = (DefaultTableModel) jTable1.getModel();
        
        String item = jComboBoxSingle.getSelectedItem().toString();
        
        o1.addOrder(item, "單點");
        
        if(v.getVoucherId(o2.getItemCode(item)) != -1)
        {
            model.insertRow(model.getRowCount(),new Object [] {"(單) "+item,"",String.valueOf(v.Calc(v.getVoucherId(o2.getItemCode(item)),o2.getPrice(o2.getItemCode(item)))),""});
        }
        else
        {
            model.insertRow(model.getRowCount(),new Object [] {"(單) "+item,"",String.valueOf(o2.getPrice(o2.getItemCode(item))),""});
        }
        
        jTable1.setRowHeight(40);//設定行高
    }//GEN-LAST:event_jButtonSingleAddMouseClicked

